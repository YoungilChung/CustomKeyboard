- (NSArray *)specialCharactersWithLetter:(NSString *)letter {

	NSArray *specialArray = @[];

	if ([letter isEqualToString:@"e"] || [letter isEqualToString:@"E"]) {
		specialArray = @[@"è", @"é", @"ê", @"ë", @"ē", @"ė", @"ę"];
	}

	if ([letter isEqualToString:@"y"] || [letter isEqualToString:@"Y"]) {
		specialArray = @[@"ÿ"];
	}

	if ([letter isEqualToString:@"u"] || [letter isEqualToString:@"U"]) {
		specialArray = @[@"û", @"ü", @"ù", @"ú", @"ū"];
	}

	if ([letter isEqualToString:@"i"] || [letter isEqualToString:@"I"]) {
		specialArray = @[@"î", @"ï", @"í", @"ī", @"į", @"ì"];
	}

	if ([letter isEqualToString:@"o"] || [letter isEqualToString:@"O"]) {
		specialArray = @[@"ô", @"ö", @"ò", @"ó", @"œ", @"ø", @"ō", @"õ"];
	}

	if ([letter isEqualToString:@"a"] || [letter isEqualToString:@"A"]) {
		specialArray = @[@"à", @"á", @"â", @"ä", @"æ", @"ã", @"å", @"ā"];
	}

	if ([letter isEqualToString:@"s"] || [letter isEqualToString:@"S"]) {
		specialArray = @[@"ß", @"ś", @"š"];
	}

	if ([letter isEqualToString:@"l"] || [letter isEqualToString:@"L"]) {
		specialArray = @[@"ł",];
	}

	if ([letter isEqualToString:@"z"] || [letter isEqualToString:@"Z"]) {
		specialArray = @[@"ž", @"ź", @"ż"];
	}

	if ([letter isEqualToString:@"c"] || [letter isEqualToString:@"C"]) {
		specialArray = @[@"ç", @"ć", @"č"];
	}

	if ([letter isEqualToString:@"n"] || [letter isEqualToString:@"N"]) {
		specialArray = @[@"ñ", @"ń"];
	}
	if ([letter isEqualToString:@"."]) {
		specialArray = @[@"?", @",", @"#", @"@", @"!"];
	}

	if ([letter isEqualToString:@"€"]) {
		specialArray = @[@"$", @"£", @"¥", @"₩", @"\u20BD"];
	}

	if ([letter isEqualToString:@"?"]) {
		specialArray = @[@"¿"];
	}
	if ([letter isEqualToString:@"!"]) {
		specialArray = @[@"¡"];
	}


	return specialArray;
}


